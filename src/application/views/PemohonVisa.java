         // Ambil nilai input dari form
        String namaPemohon = txtNik.getText().trim();
        String noPaspor = txtNoPaspor.getText().trim();
        String jenisVisa = txtJenisVisa.getText().trim();

        // Log input yang diterima
        System.out.println("Nama Pemohon: " + namaPemohon);
