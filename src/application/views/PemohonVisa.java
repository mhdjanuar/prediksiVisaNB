        public void getAllDataPemohon() {
            // Ambil data pemohon dari database
            List<PemohonVisaModel> pemohonList = pemohonDao.findAll();

            // Set Model untuk JTable
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(new Object[]{
                "ID", "Nama Pemohon", "No. Paspor", "Jenis Visa"
            });

            // Masukkan data pemohon ke dalam model JTable
            for (PemohonVisaModel pemohon : pemohonList) {
                model.addRow(new Object[]{
                    pemohon.getId(),
                    pemohon.getNamaPemohon(),
                    pemohon.getNoPaspor(),
                    pemohon.getJenisVisa(),
                });
            }

            // Set model ke JTable
            jTable1.setModel(model);

            // Sembunyikan kolom pertama (ID)
            jTable1.getColumnModel().getColumn(0).setMinWidth(0);
            jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
            jTable1.getColumnModel().getColumn(0).setWidth(0);
        }

    
