    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         // Ambil nilai input dari form
        String namaPemohon = txtNik.getText().trim();
        String noPaspor = txtNoPaspor.getText().trim();
        String jenisVisa = txtJenisVisa.getText().trim();

        // Validasi input wajib
        if (namaPemohon.isEmpty() || noPaspor.isEmpty() || jenisVisa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi.");
            return;
        }

        // Set ke model
        PemohonVisaModel pemohon = new PemohonVisaModel();
        pemohon.setId(Integer.parseInt(this.selectedId)); // ID pemohon yang akan diupdate
        pemohon.setNamaPemohon(namaPemohon);
        pemohon.setNoPaspor(noPaspor);
        pemohon.setJenisVisa(jenisVisa);

        // Panggil fungsi update di DAO
        int result = pemohonDao.update(pemohon);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Data pemohon berhasil diperbarui.");
            getAllDataPemohon();  // Refresh tabel
            clearForm();   // Bersihkan form
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data pemohon.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
      
            int success = pemohonDao.deletePemohon(parseInt(this.selectedId));
            if (success > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus.");
                getAllDataPemohon(); // reload data tabel
                clearForm();
            } else {
                JOptionPane.showMessageDialog(null, "Gagal menghapus data.");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        try{
            String templateName = "LaporanMasterDataVisa.jrxml";
            InputStream reportStream = MenuView.class.getResourceAsStream("/resources/reports/" + templateName);
            JasperDesign jd = JRXmlLoader.load(reportStream);
            
            Connection dbConnection = DatabaseUtil.getInstance().getConnection();
            JasperReport jr = JasperCompileManager.compileReport(jd);
            
            HashMap parameter = new HashMap();
            parameter.put("PATH","resources/images/");

            JasperPrint jp = JasperFillManager.fillReport(jr,parameter, dbConnection);
            JasperViewer.viewReport(jp, false);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed


