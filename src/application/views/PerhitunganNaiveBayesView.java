    public PerhitunganNaiveBayesView() {
        initComponents();
        this.dataTrainingDao = new DataTrainingDaoImpl();
        
        List<DataItem> trainingData = dataTrainingDao.findAsDataItem();
           
        // Buat objek klasifikasi dengan data training
        NaiveBayesAlgoritma nb = new NaiveBayesAlgoritma(trainingData);

        // Hitung probabilitas setiap label
        Map<String, Double> probabilities = nb.getLabelProbabilities(trainingData);

        // Buat model tabel dengan kolom Label dan Probabilitas
        DefaultTableModel model = new DefaultTableModel(new Object[]{"Label", "Probabilitas"}, 0);

        // Isi data ke dalam tabel
        for (Map.Entry<String, Double> entry : probabilities.entrySet()) {
            String label = entry.getKey();
            double prob = entry.getValue();

            // Tambahkan baris ke model
            model.addRow(new Object[]{label, String.format("%.4f", prob)});
        }

        // Set model ke jTable1
        jTable1.setModel(model);
        
        tampilkanProbabilitasKategori("Lolos Cepat", jTable3);
        tampilkanProbabilitasKategori("Terhambat", jTable2);
    }

