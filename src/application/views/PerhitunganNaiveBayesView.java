    private void tampilkanProbabilitasKategori(String label, JTable table) {
        // Contoh data
        List<DataItem> trainingData = dataTrainingDao.findAsDataItem();

        NaiveBayesAlgoritma nb = new NaiveBayesAlgoritma(trainingData);
        
        Map<String, Map<String, Double>> featureProbs = nb.getFeatureProbabilitiesPerLabel(label);

        String[] columnNames = { "Fitur", "Nilai Kategori", "Probabilitas" };
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        for (String feature : featureProbs.keySet()) {
            Map<String, Double> kategoriMap = featureProbs.get(feature);
            for (Map.Entry<String, Double> entry : kategoriMap.entrySet()) {
                String kategori = entry.getKey();
                Double probabilitas = entry.getValue();
                model.addRow(new Object[] { feature, kategori, String.format("%.4f", probabilitas) });
            }
        }

        table.setModel(model);
    }


